const students = {
    student1: {
        subject1: 44,
        subject2: 56,
        subject3: 87,
        subject4: 97,
        subject5: 37
    },
    student2: {
        subject1: 44,
        subject2: 56,
        subject3: 87,
        subject4: 97,
        subject5: 37
    },
    student3: {
        subject1: 44,
        subject2: 56,
        subject3: 87,
        subject4: 97,
        subject5: 37
    }
};

function avgMarks(students){
    return Object.keys(students).map(student =>{
        let marks = Object.values(students[student]);
        let total = marks.reduce((acc, curr) => 
            acc + curr, 0);
        let average= total/marks.length;

        return { [student] : {average: Math.floor(average)}}
        })
    }

console.log(avgMarks(students))